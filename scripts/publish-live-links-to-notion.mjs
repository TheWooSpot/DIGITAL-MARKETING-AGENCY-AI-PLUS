/**
 * Creates a Notion page with production door/route URLs (markdown content → blocks).
 *
 * Prerequisites:
 * 1. Create an integration: https://www.notion.so/my-integrations → copy "Internal integration secret"
 * 2. Open the Notion page that should be the parent → "..." → Add connections → your integration
 * 3. Copy the parent page ID from the URL:
 *    https://www.notion.so/Your-Title-abc123def456...  → use the id part (32 chars, with or without dashes)
 *
 * Run (PowerShell):
 *   $env:NOTION_TOKEN="secret_xxxxxxxx"
 *   $env:NOTION_PARENT_PAGE_ID="xxxxxxxx-xxxx-xxxx-xxxx-xxxxxxxxxxxx"
 *   node scripts/publish-live-links-to-notion.mjs
 *
 * Or one line:
 *   NOTION_TOKEN=... NOTION_PARENT_PAGE_ID=... node scripts/publish-live-links-to-notion.mjs
 */

const NOTION_API = "https://api.notion.com/v1";
const NOTION_VERSION = "2022-06-28";

const ORIGIN = "https://socialutely-any-door-engine.vercel.app";

const ROWS_LIVE = [
  ["D-2", "The Mirror — URL diagnostic (engineering: Door B1)", `${ORIGIN}/diagnostic`],
  ["D-2", "Alias route", `${ORIGIN}/doors/url-diagnostic`],
];

const ROWS_OTHER = [
  ["/ai-iq", `${ORIGIN}/ai-iq`],
  ["/ai-iq/report", `${ORIGIN}/ai-iq/report`],
  ["/calculator", `${ORIGIN}/calculator`],
  ["/self-discovery", `${ORIGIN}/self-discovery`],
  ["/dream", `${ORIGIN}/dream`],
  ["/quote", `${ORIGIN}/quote`],
  ["/diagnostic/results", `${ORIGIN}/diagnostic/results`],
  ["/diagnostic/unlock", `${ORIGIN}/diagnostic/unlock`],
  ["/report/{token}", `${ORIGIN}/report/{token}`],
  ["/your-package", `${ORIGIN}/your-package`],
  ["/team/tiers", `${ORIGIN}/team/tiers`],
  ["/ai-readiness/rung-2", `${ORIGIN}/ai-readiness/rung-2`],
  ["/ai-readiness/rung-3", `${ORIGIN}/ai-readiness/rung-3`],
  ["/contact", `${ORIGIN}/contact`],
  ["/privacy", `${ORIGIN}/privacy`],
  ["/#doors", `${ORIGIN}/#doors`],
];

function rtPlain(text) {
  return [{ type: "text", text: { content: text } }];
}

function heading2(text) {
  return {
    object: "block",
    type: "heading_2",
    heading_2: { rich_text: rtPlain(text) },
  };
}

function para(text) {
  return {
    object: "block",
    type: "paragraph",
    paragraph: { rich_text: rtPlain(text) },
  };
}

/** Prefix (plain) + clickable URL */
function bulletPrefixUrl(prefix, url) {
  return {
    object: "block",
    type: "bulleted_list_item",
    bulleted_list_item: {
      rich_text: [
        { type: "text", text: { content: prefix } },
        { type: "text", text: { content: url, link: { url } } },
      ],
    },
  };
}

function buildChildren() {
  const blocks = [
    para(`Site origin: ${ORIGIN} (change in script if you use a custom domain).`),
    heading2("Doors marked LIVE (Supabase)"),
    para("Official LIVE door in CMS: D-2 · The Mirror (= Door B1 in engineering)."),
    ...ROWS_LIVE.map(([a, b, u]) => bulletPrefixUrl(`${a} — ${b}: `, u)),
    heading2("Other deployed routes"),
    para("Marked building/planned in CMS; URLs still resolve."),
    ...ROWS_OTHER.map(([path, u]) => bulletPrefixUrl(`${path} → `, u)),
    para("Generated by scripts/publish-live-links-to-notion.mjs"),
  ];
  return blocks;
}

function normalizePageId(raw) {
  const s = String(raw).replace(/-/g, "");
  if (s.length !== 32) throw new Error("NOTION_PARENT_PAGE_ID must be 32 hex chars (with or without dashes).");
  return `${s.slice(0, 8)}-${s.slice(8, 12)}-${s.slice(12, 16)}-${s.slice(16, 20)}-${s.slice(20, 32)}`;
}

async function main() {
  const token = process.env.NOTION_TOKEN?.trim();
  const parentRaw = process.env.NOTION_PARENT_PAGE_ID?.trim();
  if (!token || !parentRaw) {
    console.error("Missing NOTION_TOKEN or NOTION_PARENT_PAGE_ID.");
    process.exit(1);
  }
  const parentId = normalizePageId(parentRaw);

  const title = "Socialutely — Production door & route URLs";

  const body = {
    parent: { page_id: parentId },
    properties: {
      title: {
        title: [{ type: "text", text: { content: title } }],
      },
    },
    children: buildChildren(),
  };

  const res = await fetch(`${NOTION_API}/pages`, {
    method: "POST",
    headers: {
      Authorization: `Bearer ${token}`,
      "Notion-Version": NOTION_VERSION,
      "Content-Type": "application/json",
    },
    body: JSON.stringify(body),
  });

  const json = await res.json().catch(() => ({}));
  if (!res.ok) {
    console.error("Notion API error:", res.status, JSON.stringify(json, null, 2));
    process.exit(1);
  }
  console.log("Created Notion page:", json.id);
  console.log("Open in Notion:", json.url ?? "(use page id in workspace)");
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
